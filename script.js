const selectedTitles = new Set([
  "Geometric Landau analysis and symbol bootstrap",
  "Complete computation of all three-loop five-point massless planar integrals",
  "Nonplanar integrated correlator in N=4 SYM",
  "Higher-loop integrated negative geometries in ABJM",
  "The orthogonal momentum amplituhedron and ABJM amplitudes",
  "Integrated correlators in N = 4 super Yang-Mills and periods",
]);

const list = document.querySelector("#publication-list");
const filters = document.querySelectorAll(".filter");

function escapeHTML(value) {
  return String(value ?? "")
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}

function escapeAttribute(value) {
  return escapeHTML(value).replaceAll("`", "&#096;");
}

function authorSummary(authors) {
  if (!authors.length) return "";
  return authors.join("; ");
}

function paperLinks(paper) {
  const links = [];
  if (paper.arxiv) {
    links.push(`<a class="tag" href="https://arxiv.org/abs/${escapeAttribute(paper.arxiv)}">arXiv:${escapeHTML(paper.arxiv)}</a>`);
  }
  if (paper.doi) {
    links.push(`<a class="tag" href="https://doi.org/${escapeAttribute(paper.doi)}">DOI</a>`);
  }
  links.push(`<span class="tag">${escapeHTML(paper.citations)} citations</span>`);
  return links.join("");
}

function paperMatches(paper, filter) {
  if (filter === "selected") return selectedTitles.has(paper.title);
  if (filter === "recent") return paper.year >= 2024;
  return true;
}

function renderPapers(filter = "all") {
  const visible = papers.filter((paper) => paperMatches(paper, filter));
  list.innerHTML = visible
    .map((paper) => {
      const venue = [paper.publication, paper.type === "thesis" ? "Thesis" : ""].filter(Boolean).join(" · ");
      return `
        <article class="paper">
          <div class="paper-year">${escapeHTML(paper.year || "")}</div>
          <div>
            <h3 class="paper-title"><a href="${escapeAttribute(paper.url)}">${escapeHTML(paper.title)}</a></h3>
            <p class="paper-meta">${escapeHTML(authorSummary(paper.authors))}</p>
            <p class="paper-meta">${escapeHTML(venue || "Preprint")}</p>
          </div>
          <div class="paper-tags">${paperLinks(paper)}</div>
        </article>
      `;
    })
    .join("");
}

function init() {
  document.querySelector("#paper-count").textContent = papers.length;
  renderPapers();

  filters.forEach((button) => {
    button.addEventListener("click", () => {
      filters.forEach((item) => item.classList.remove("is-active"));
      button.classList.add("is-active");
      renderPapers(button.dataset.filter);
    });
  });
}

init();
